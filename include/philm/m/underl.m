function understring=underl(instr,mychar)
% UNDERL Generate string of characters like underline equal in length to input string
% function understring=underl(instr,mychar)
% mychar: Optional. Desired underline character. Default is '=' (sic!)

myc='=';
if nargin>1
   myc=mychar;
end;

understring=repmat(myc,[1 length(instr)]);
